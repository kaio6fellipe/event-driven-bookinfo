func TestBatchReplay(t *testing.T) {
	ctx := context.Background()
	repo := memory.NewDLQRepository()
	client := &fakeReplayClient{}
	svc := service.NewDLQService(repo, client, 3)

	// 3 distinct pending events — distinct sensor OR trigger OR payload to avoid dedup.
	for i := 0; i < 3; i++ {
		p := newParams("evt-" + string(rune('a'+i)))
		p.SensorName = "sensor-" + string(rune('a'+i))
		p.OriginalPayload = []byte(`{"i":"` + string(rune('0'+i)) + `"}`)
		_, _ = svc.IngestEvent(ctx, p)
	}

	n, err := svc.BatchReplay(ctx, port.BatchFilter{Status: string(domain.StatusPending), Limit: 10})
	if err != nil {
		t.Fatalf("BatchReplay err = %v", err)
	}
	if n != 3 {
		t.Errorf("replayed count = %d, want 3", n)
	}
	if len(client.calls) != 3 {
		t.Errorf("replay client calls = %d, want 3", len(client.calls))
	}
}

