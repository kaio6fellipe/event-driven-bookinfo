func TestIngest_Duplicate_IncrementsRetry(t *testing.T) {
	ctx := context.Background()
	repo := memory.NewDLQRepository()
	svc := service.NewDLQService(repo, &fakeReplayClient{}, 3)

	// First ingest.
	e1, _ := svc.IngestEvent(ctx, newParams("evt-1"))
	// Simulate that it was replayed.
	_ = e1.Replay()
	_ = repo.Update(ctx, e1)

	// Same payload+sensor+trigger arrives again (new event_id generated by Argo).
	params := newParams("evt-2-different-envelope-id")
	e2, err := svc.IngestEvent(ctx, params)
	if err != nil {
		t.Fatalf("IngestEvent err = %v", err)
	}
	if e2.ID != e1.ID {
		t.Errorf("expected same DLQ ID after dedup, got %q (orig %q)", e2.ID, e1.ID)
	}
	if e2.RetryCount != 1 {
		t.Errorf("retry_count = %d, want 1", e2.RetryCount)
	}
	if e2.Status != domain.StatusPending {
		t.Errorf("status = %v, want pending", e2.Status)
	}
}

